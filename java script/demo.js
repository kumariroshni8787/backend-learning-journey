// Operator Precedence Practice

let age = 22;
let marks = 75;
let isStudent = true;

// Expression 1
let result1 = age > 18 && marks > 50 || false;

// Expression 2
let result2 = age < 18 || marks > 50 && isStudent;

// Expression 3
let result3 = (age > 18 && marks < 50) || isStudent;

// Expression 4
let result4 = !(age < 18) && marks >= 75;

// Expression 5
let result5 = age > 25 || (marks >= 70 && isStudent);

console.log("Result 1:", result1);
console.log("Result 2:", result2);
console.log("Result 3:", result3);
console.log("Result 4:", result4);
console.log("Result 5:", result5);
