var firstName = "Roshni";
var lastName = "Kumari";
var city = "Ramgarh";
var age = 23;

// String concatenation
console.log(firstName + lastName);

// Adding space
console.log(firstName + " " + lastName);

// Full sentence
console.log("My name is " + firstName + " " + lastName + " and I live in " + city);

// Number with string
console.log("My age is " + age);

// Mixed example
var x = 10;
var y = 20;

console.log("The sum of " + x + " and " + y + " is " + (x + y));